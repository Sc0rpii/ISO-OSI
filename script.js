function switchPage(index){
    switch (index){
        case 0: window.location = "./layer/applicationd.html";
        break;
        case 1: window.location = "./layer/presentation.html";
        break;
        case 2: window.location = "./layer/session.html";
        break;
        case 3: window.location = "./layer/transport.html";
        break;
        case 4: window.location = "./layer/network.html";
        break;
        case 5: window.location = "./layer/datalink.html";
        break;
        case 6: window.location = "./layer/physical.html";
        break;
    }
}